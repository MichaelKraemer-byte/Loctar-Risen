    animate(){

        // Offset Refresh
        setInterval(() => {
            this.refreshOffset();
        }, 1000 / 25);

        // WALK
        setInterval(() => {
            if (this.isVisible() && this.HP > 0 && !this.damageProcess) {
                this.walkLeft();
                // this.playSound(this.walkSound, 0.02, 1);
        //     } else if (!this.isVisible()) {
        //         this.pauseSound(this.walkSound);
            }
        }, 1000 / 25);

        setInterval(() => {
            if (this.HP > 0 && !this.damageProcess) {
                this.playAnimation(this.walkImages);
            };
        }, 50);


        // HURT Images
        setInterval(() => {
            if (this.damageProcess && this.HP > 0) {
                this.playAnimation(this.hurtImages);
            }
        }, 70);


        // DYING Images
        setInterval(() => {
            if (this.isDead()) {
                this.playSingleAnimation(this.dyingImages);
            };
        }, 100);

    };
    


